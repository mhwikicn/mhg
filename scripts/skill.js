function SortSkill()
{
  numSkGrp = 0;
  for (i=0; i<65; i++) {
    if (skillGen[i] != 99) {
      if (i == 41) {
        total = -99;
      } else if (i == 42) {
        total = -99;
      } else if (i == 43) {
        total = -99;
      } else {
        total = skillAta[i] + skillDou[i] + skillUde[i] + skillKos[i] + skillAsi[i];
        if (skillDou[i] != 0)  total += skillDov[i];
      }
      skillTot[numSkGrp] = total;
      skillOrd[numSkGrp] = i;
      numSkGrp++;
    }
  }
  for (i=0; i<numSkGrp-1; i++) {
    for (j=i+1; j<numSkGrp; j++) {
      if (skillTot[i] <= skillTot[j]) {
        temp = skillTot[i];
        skillTot[i] = skillTot[j];
        skillTot[j] = temp;
        temp = skillOrd[i];
        skillOrd[i] = skillOrd[j];
        skillOrd[j] = temp;
      }
    }
  }
  for (i=0; i<numSkGrp-1; i++) {
    for (j=i+1; j<numSkGrp; j++) {
      if (skillTot[i] < 0 && skillTot[i] > -90 &&
          skillTot[j] < 0 && skillTot[j] > -90) {
        if (skillTot[i] > skillTot[j]) {
          temp = skillTot[i];
          skillTot[i] = skillTot[j];
          skillTot[j] = temp;
          temp = skillOrd[i];
          skillOrd[i] = skillOrd[j];
          skillOrd[j] = temp;
        }
      }
    }
  }
}


